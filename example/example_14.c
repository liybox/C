/*
题目：打印出所有的"水仙花数"，所谓"水仙花数"是指一个三位数，其各位数字立方和等于该数 本身。例如：153是一个"水仙花数"，因为153=1的三次方5的三次方3的三次方。

程序分析：利用for循环控制100-999个数，每个数分解出个位，十位，百位。
*/

#include <stdio.h>

int main()
{
    int a, b, c;

    for (int i = 100; i < 1000; i++)
    {
        a = i / 100;
        b = i % 100 / 10;
        c = i % 10;
        // printf("%d%d%d\n",a,b,c);
        if (i == a * a * a + b * b * b + c * c * c)
        {
            printf("水仙花数：%d \n", i);
        }
    }

    return 0;
}
